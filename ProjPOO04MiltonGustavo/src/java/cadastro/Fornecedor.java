package cadastro;
public class Fornecedor {
 
    private String nomeF;
    private String rsF;
    private String cnpjF;
    private String emailF;
    private String telF;
    private String ruaF;
    private String bairroF;
    private String lograF; 
    private String localF; 
    private String cepF;     
    
    public String getNomeF() {
        return nomeF;
    }
    public void setNomeF(String nomeF) {
        this.nomeF = nomeF;
    }

    public String getRsF() {
        return rsF;
    }
    public void setRsF(String rsF) {
        this.rsF = rsF;
    }

    public String getCnpjF() {
        return cnpjF;
    }
    public void setCnpjF(String cnpjF) {
        this.cnpjF = cnpjF;
    }

    public String getEmailF() {
        return emailF;
    }
    public void setEmailF(String emailF) {
        this.emailF = emailF;
    }

    public String getTelF() {
        return telF;
    }
    public void setTelF(String telF) {
        this.telF = telF;
    }

    public String getRuaF() {
        return ruaF;
    }
    public void setRuaF(String ruaF) {
        this.ruaF = ruaF;
    }    
    
    public String getBairroF() {
        return bairroF;
    }
    public void setBairroF(String bairroF) {
        this.bairroF = bairroF;
    }

    public String getLograF() {
        return lograF;
    }
    public void setLograF(String lograF) {
        this.lograF = lograF;
    }

    public String getLocalF() {
        return localF;
    }
    public void setLocalF(String localF) {
        this.localF = localF;
    }    

    public String getCepF() {
        return cepF;
    }
    public void setCepF(String cepF) {
        this.cepF = cepF;
    }       
    
}

