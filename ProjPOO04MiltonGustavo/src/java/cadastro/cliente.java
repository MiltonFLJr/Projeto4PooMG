public class cliente {
    
    private String nomeC;
    private String cpfC;
    private String rgC;
    private String emailC;
    private String telefoneC;
    private String ruaC;
    private String bairroC;
    private String numeroC; 
    private String localidadeC; 
    private String cepC; 

    public String getNomeC() {
        return nomeC;
    }

    public void setNomeC(String nomeC) {
        this.nomeC = nomeC;
    }

    public String getCpfC() {
        return cpfC;
    }

    public void setCpfC(String cpfC) {
        this.cpfC = cpfC;
    }

    public String getRgC() {
        return rgC;
    }

    public void setRgC(String rgC) {
        this.rgC = rgC;
    }

    public String getEmailC() {
        return emailC;
    }

    public void setEmailC(String emailC) {
        this.emailC = emailC;
    }

    public String getTelefoneC() {
        return telefoneC;
    }

    public void setTelefoneC(String telefoneC) {
        this.telefoneC = telefoneC;
    }

    public String getRuaC() {
        return ruaC;
    }

    public void setRuaC(String ruaC) {
        this.ruaC = ruaC;
    }

    public String getBairroC() {
        return bairroC;
    }

    public void setBairroC(String bairroC) {
        this.bairroC = bairroC;
    }

    public String getNumeroC() {
        return numeroC;
    }

    public void setNumeroC(String numeroC) {
        this.numeroC = numeroC;
    }

    public String getLocalidadeC() {
        return localidadeC;
    }

    public void setLocalidadeC(String localidadeC) {
        this.localidadeC = localidadeC;
    }

    public String getCepC() {
        return cepC;
    }

    public void setCepC(String cepC) {
        this.cepC = cepC;
    }
        
}
